/*
- Primitivos (imutáveis): string, number, boolean, undefined, null
- Referência (mutável): array, object, function 
*/
// Os valores primitivos ao serem passados para uma variável são copiados: let x = y. Mesmo que "y" seja alterado mais a frente do código, o valor de "x" será o mesmo de quando foi copiado.
// Os valores de referência ao serem passados para uma váriavel são justamente referências: let x = [y]. Se o valor de "[y]" for alterado em algum momento do código, o valor de "x" também será alterado.