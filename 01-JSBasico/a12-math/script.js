const num1 = 19.3456;

// Utilizando Math.floor(numeroAqui) arredondamos o número para baixo
console.log(Math.floor(num1));

// Utilizando Math.ceil(numeroAqui) arredondamos o número para cima
console.log(Math.ceil(num1));

// Utilizando Math.round(numeroAqui) arredondamos o número para o número inteiro mais próximo
console.log(Math.round(num1));

// Math.max(listaAqui) exibe o maior número da lista
console.log(Math.max(1, 4, 13, -5, 34));

// Math.min(listaAqui) exibe o menor número da lista
console.log(Math.min(1, 4, 13, -5, 34));

// Math.random() exibe um número aleatório
console.log(Math.random());