/*
Operadores de comparação

- > : Maior que
- >= : Maior ou igual a
- < : Menor que
- <= Menor ou igual a
- == Igualdade (valor)
- === Igualdade estrita (valor e tipo)
- != Diferente (valor)
- !== Diferente estrito (valor e tipo)
*/

let num1 = 100;
let num2 = '100';

console.log(num1 == num2);
console.log(num1 === num2);
